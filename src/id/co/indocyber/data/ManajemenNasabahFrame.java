    private void showPeople() {
        DefaultTableModel model = (DefaultTableModel) tabelNasabah.getModel();
        List<DataNasabah> dataNasabah = getDataFromFile("C:\\JAVA_LATIHAN\\ManajemenNasabah.txt");

        Object[] row = new Object[2];
        for (DataNasabah dNasabah : dataNasabah) {
            row[0] = dNasabah.getKode();
            row[1] = dNasabah.getName();
            model.addRow(row);
        }

        ListSelectionModel rowModel = tabelNasabah.getSelectionModel();
        rowModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int i = tabelNasabah.getSelectedRow();
                    String id = (String) model.getValueAt(i, 0);//ambil velue row i 
                    idTextField.setText(id);
                    String nama = (String) model.getValueAt(i, 1);//ambil velue row i 
                    nameTextField.setText(nama);
                    String gender = dataNasabah.get(i).getGender();
                    genderComboBox.setSelectedItem(gender);
                    Boolean maried = dataNasabah.get(i).getMarried();
                    marriedCheckBox.setSelected(maried);
                    Integer age = dataNasabah.get(i).getAge();
                    String umur = String.valueOf(age);
                    ageTextField.setText(umur);
                    noHpTextField.setText(dataNasabah.get(i).getNoHpNasabah());
                    teleponTextField.setText(dataNasabah.get(i).getNoTeleponNasabah());
                    alamatTextArea.setText(dataNasabah.get(i).getAlamatNasabah());

                    System.out.println("row dipilih");
                }
            }
        });

    }

    private List<DataNasabah> getDataFromFile(String filename) {
        FileReader fileReader = null;
        try {
            List<DataNasabah> dataNasabah = new ArrayList<>();
            File file = new File(filename);
            fileReader = new FileReader(file); //mencari file
            BufferedReader reader = new BufferedReader(fileReader);// untuk memanggil filereader/membaca file
            String line = null;
            while ((line = reader.readLine()) != null) {
                System.out.println(line);
                //Split line assign ke strig array
                String[] splitLine = line.split(",");
                //buat objek questionmodel, array idx 0 kata dasar, idx 1 untuk petunjuk
                DataNasabah wm = new DataNasabah(splitLine[0], splitLine[1].trim(), splitLine[2].trim(), Boolean.parseBoolean(splitLine[3].trim()), Integer.parseInt(splitLine[4].trim()), splitLine[5].trim(), splitLine[6].trim(), splitLine[7].trim());
                // add ke question collection
                dataNasabah.add(wm);
            }
            Collections.sort(dataNasabah);
            //periksa ukuran question
            System.out.println("\nJumlah Pertanyaan Dalam Collection : " + dataNasabah.size() + "\n");
            return dataNasabah;
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ManajemenNasabahFrame.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("File Tidak Ditemukan");
        } catch (IOException ex) {
            Logger.getLogger(ManajemenNasabahFrame.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Kemungkinan File Rusak");
        } finally {
            try {
                fileReader.close();
            } catch (IOException ex) {
                Logger.getLogger(ManajemenNasabahFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return null;
    }


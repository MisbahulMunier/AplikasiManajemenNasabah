    /*
    Method setting ukutan kolom tabel
     */
    private void settingUkuranTabel(javax.swing.JTable tabel, int lebar[]) {
        tabel.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        int kolom = tabel.getColumnCount();
        for (int i = 0; i < kolom; i++) {
            javax.swing.table.TableColumn kolomTabel = tabel.getColumnModel().getColumn(i);
            kolomTabel.setPreferredWidth(lebar[i]);
            tabel.setRowHeight(24);
        }
    }

